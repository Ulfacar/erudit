function table(headers, rows, colWidths) {
  const widthPct = Math.floor(100 / headers.length);
  const headerRow = new TableRow({
    tableHeader: true,
    children: headers.map((h) => new TableCell({
      shading: { type: ShadingType.CLEAR, fill: ACCENT },
      margins: { top: 60, bottom: 60, left: 100, right: 100 },
      children: [new Paragraph({ children: [new TextRun({ text: h, bold: true, color: 'FFFFFF', size: 20 })] })],
    })),
  });
  const bodyRows = rows.map((cells, i) => new TableRow({
    children: cells.map((c, ci) => new TableCell({
      shading: i % 2 ? { type: ShadingType.CLEAR, fill: LIGHT } : undefined,
      width: { size: colWidths ? colWidths[ci] : widthPct, type: WidthType.PERCENTAGE },
      margins: { top: 50, bottom: 50, left: 100, right: 100 },
      children: (Array.isArray(c) ? c : [c]).map((line) =>
        new Paragraph({ children: [new TextRun({ text: line, size: 20, color: DARK })] })),
    })),
  }));
  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    borders: {
      top: { style: BorderStyle.SINGLE, size: 2, color: 'DDDDDD' },
      bottom: { style: BorderStyle.SINGLE, size: 2, color: 'DDDDDD' },
      left: { style: BorderStyle.SINGLE, size: 2, color: 'DDDDDD' },
      right: { style: BorderStyle.SINGLE, size: 2, color: 'DDDDDD' },
      insideHorizontal: { style: BorderStyle.SINGLE, size: 1, color: 'EEEEEE' },
      insideVertical: { style: BorderStyle.SINGLE, size: 1, color: 'EEEEEE' },
    },
    rows: [headerRow, ...bodyRows],
  });
}

