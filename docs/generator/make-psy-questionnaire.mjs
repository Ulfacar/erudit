/* ---------- хелперы ---------- */
const TITLE = (t) => new Paragraph({
  heading: HeadingLevel.TITLE, spacing: { after: 80 },
  children: [new TextRun({ text: t, bold: true, color: ACCENT, size: 40 })],
});
const H1 = (t) => new Paragraph({
  heading: HeadingLevel.HEADING_1, spacing: { before: 340, after: 130 },
  children: [new TextRun({ text: t, bold: true, color: ACCENT, size: 28 })],
});
const P = (runs, opts = {}) => new Paragraph({
  spacing: { after: 110, line: 276 }, alignment: opts.align,
  children: (Array.isArray(runs) ? runs : [runs]).map((r) =>
    typeof r === 'string' ? new TextRun({ text: r, size: 22, color: DARK }) : r),
});
const bold = (t) => new TextRun({ text: t, bold: true, size: 22, color: DARK });
const txt = (t) => new TextRun({ text: t, size: 22, color: DARK });
const italic = (t) => new TextRun({ text: t, italics: true, size: 21, color: GREY });

// Вопрос: жирная строка-вопрос + пустая линия под ответ.
const Q = (n, t) => new Paragraph({
  spacing: { before: 130, after: 30 },
  children: [new TextRun({ text: `${n}. `, bold: true, size: 22, color: ACCENT }),
             new TextRun({ text: t, size: 22, color: DARK })],
});
const ANS = () => new Paragraph({
  spacing: { after: 90 },
  children: [new TextRun({ text: 'Ответ: ', size: 21, color: GREY }),
             new TextRun({ text: '_'.repeat(80), size: 21, color: 'CCCCCC' })],
});
// Вариант с чекбоксом.
const OPT = (t) => new Paragraph({
  spacing: { after: 20 }, indent: { left: 360 },
  children: [new TextRun({ text: '☐  ', size: 22, color: ACCENT }),
             new TextRun({ text: t, size: 22, color: DARK })],
});
const NOTE = (t) => new Paragraph({
  spacing: { after: 110 }, indent: { left: 360 },
  children: [new TextRun({ text: t, italics: true, size: 20, color: GREY })],
});

function infoBox(lines) {
  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    borders: {
      top: { style: BorderStyle.SINGLE, size: 4, color: ACCENT },
      bottom: { style: BorderStyle.SINGLE, size: 4, color: ACCENT },
      left: { style: BorderStyle.SINGLE, size: 4, color: ACCENT },
      right: { style: BorderStyle.SINGLE, size: 4, color: ACCENT },
      insideHorizontal: { style: BorderStyle.NONE },
      insideVertical: { style: BorderStyle.NONE },
    },
    rows: [new TableRow({
      children: [new TableCell({
        shading: { type: ShadingType.CLEAR, fill: LIGHT },
        margins: { top: 120, bottom: 120, left: 160, right: 160 },
        children: lines.map((l) => new Paragraph({
          spacing: { after: 50 },
          children: (Array.isArray(l) ? l : [l]).map((r) =>
            typeof r === 'string' ? new TextRun({ text: r, size: 21, color: DARK }) : r),
        })),
      })],
    })],
  });
}

