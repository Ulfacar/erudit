function done(doc, path) {
  return new Promise((res, rej) => {
    const s = createWriteStream(path);
    doc.pipe(s);
    s.on('finish', res); s.on('error', rej);
    doc.end();
  });
}

/* ============ helper: table renderer with page breaks ============ */
function drawTable(doc, x, startY, colW, rows, opts = {}) {
  const pad = 6, fs = opts.fs || 10, lh = fs + 4;
  let y = startY;
  const bottom = doc.page.height - doc.page.margins.bottom;

  rows.forEach((row, ri) => {
    // measure row height
    const heights = row.cells.map((c, i) => {
      doc.font(c.bold || row.header ? BF : RF).fontSize(fs);
      const h = doc.heightOfString(c.text, { width: colW[i] - pad * 2 });
      return h;
    });
    const rowH = Math.max(...heights) + pad * 2;

    if (y + rowH > bottom) { doc.addPage(); y = doc.page.margins.top; }

    let cx = x;
    row.cells.forEach((c, i) => {
      const fill = row.header ? ACCENT : (ri % 2 === 0 ? LIGHT : '#FFFFFF');
      doc.rect(cx, y, colW[i], rowH).fillAndStroke(fill, '#E2E2E2');
      doc.font(c.bold || row.header ? BF : RF).fontSize(fs)
        .fillColor(row.header ? '#FFFFFF' : (c.color || DARK))
        .text(c.text, cx + pad, y + pad, { width: colW[i] - pad * 2, align: c.align || 'left' });
      cx += colW[i];
    });
    y += rowH;
  });
  doc.fillColor(DARK);
  return y;
}

