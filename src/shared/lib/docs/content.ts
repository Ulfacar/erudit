export function getArticle(id: string): DocArticle | undefined {
  return DOC_ARTICLES.find((article) => article.id === id);
}

export function articlesByModule(moduleId: string): DocArticle[] {
  return DOC_ARTICLES.filter((article) => article.moduleId === moduleId);
}

export function articlesForRole(role: Role): DocArticle[] {
  const guide = ROLE_GUIDES.find((item) => item.role === role);
  return guide?.articleIds.map(getArticle).filter((article): article is DocArticle => Boolean(article)) ?? [];
}

export function searchArticles(q: string): DocArticle[] {
  const s = q.trim().toLowerCase();
  if (!s) return [];

  return DOC_ARTICLES.filter((article) => {
    return (
      article.title.toLowerCase().includes(s) ||
      article.summary.toLowerCase().includes(s) ||
      article.steps.some((step) => step.text.toLowerCase().includes(s))
    );
  });
}
